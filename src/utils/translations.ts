export const useTranslation = (language: 'es' | 'en' = 'es') => {
  return (key: string): string => {
    const keys = key.split('.');
    let value: any = translations[language];
    
    for (const k of keys) {
      value = value?.[k];
    }
    
    return value || key;
  };
};